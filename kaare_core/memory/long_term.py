    def count_unverified_since_last(self, user_id: str = USER_GLOBAL) -> tuple[int, int, int]:
        """
        Teller interaksjoner med outcome=success og feedback=unknown
        siden siste verifikasjonsvindu for denne brukeren.

        Returnerer (antall_ubekreftede, from_id, to_id).
        """
        conn = _get_conn()
        try:
            row = conn.execute(
                "SELECT to_id FROM verifications WHERE user_id=? ORDER BY id DESC LIMIT 1",
                (user_id,),
            ).fetchone()
            after_id = row[0] if row else 0

            cur = conn.execute(
                """SELECT COUNT(*), MIN(id), MAX(id) FROM interactions
                   WHERE id > ? AND user_id = ? AND outcome = 'success' AND feedback = 'unknown'""",
                (after_id, user_id),
            )
            count, from_id, to_id = cur.fetchone()
            return (count or 0, from_id or 0, to_id or 0)
        finally:
            conn.close()

    def open_verification(self, from_id: int, to_id: int, count: int, user_id: str = USER_GLOBAL) -> Optional[int]:
        """
        Oppretter en verifikasjonsrad med verdict='pending'.
        Returnerer verifikasjons-ID.
        """
        conn = _get_conn()
        try:
            cur = conn.execute(
                """INSERT INTO verifications (ts_asked, user_id, from_id, to_id, unverified_count, verdict)
                   VALUES (?, ?, ?, ?, ?, 'pending')""",
                (_utc_ts(), user_id, from_id, to_id, count),
            )
            conn.commit()
            return cur.lastrowid
        except Exception as e:
            logger.warning("LTM: open_verification feilet: %s", e)
            return None
        finally:
            conn.close()

    def close_verification(
        self, verification_id: int, verdict: str, user_text: str = ""
    ) -> None:
        """
        Oppdaterer verifikasjonsvinduet med brukerens svar.
        verdict: 'confirmed' | 'denied'
        Justerer confidence på interaksjonene i vinduet.
        """
        conn = _get_conn()
        try:
            uid_row = conn.execute("SELECT user_id FROM verifications WHERE id=?", (verification_id,)).fetchone()
            uid = uid_row[0] if uid_row else USER_GLOBAL
            conn.execute(
                """UPDATE verifications
                   SET verdict=?, ts_answered=?, user_text=?
                   WHERE id=?""",
                (verdict, _utc_ts(), _enc(uid, user_text), verification_id),
            )
            # Juster confidence på interaksjonene i vinduet
            new_confidence = 0.9 if verdict == "confirmed" else 0.1
            row = conn.execute(
                "SELECT from_id, to_id FROM verifications WHERE id=?",
                (verification_id,),
            ).fetchone()
            if row:
                conn.execute(
                    """UPDATE interactions SET confidence=?
                       WHERE id BETWEEN ? AND ? AND outcome='success' AND feedback='unknown'""",
                    (new_confidence, row[0], row[1]),
                )
                # Merk feedback på samme vindu
                new_feedback = "verified" if verdict == "confirmed" else "denied_general"
                conn.execute(
                    """UPDATE interactions SET feedback=?
                       WHERE id BETWEEN ? AND ? AND outcome='success' AND feedback='unknown'""",
                    (new_feedback, row[0], row[1]),
                )
            conn.commit()
        except Exception as e:
            logger.warning("LTM: close_verification feilet: %s", e)
        finally:
            conn.close()

    def get_pending_verification(self, user_id: str = USER_GLOBAL) -> Optional[dict]:
        """Returnerer åpent verifikasjonsvindu for denne brukeren, eller None."""
        conn = _get_conn()
        try:
            row = conn.execute(
                """SELECT id, ts_asked, from_id, to_id, unverified_count
                   FROM verifications WHERE verdict='pending' AND user_id=?
                   ORDER BY id DESC LIMIT 1""",
                (user_id,),
            ).fetchone()
            if not row:
                return None
            return {
                "id": row[0], "ts_asked": row[1],
                "from_id": row[2], "to_id": row[3], "count": row[4],
            }
        finally:
            conn.close()

    def get_unverified_interactions(
        self, user_id: str = USER_GLOBAL, limit: int = 10, offset: int = 0
    ) -> list[dict]:
        """
        Returns unverified interactions (feedback='unknown', outcome='success').
        Ordered oldest first so reviews progress chronologically.
        """
        conn = _get_conn()
        try:
            rows = conn.execute(
                """SELECT id, ts, prompt, response FROM interactions
                   WHERE user_id = ? AND outcome = 'success' AND feedback = 'unknown'
                   ORDER BY id ASC
                   LIMIT ? OFFSET ?""",
                (user_id, limit, offset),
            ).fetchall()
            return [
                {
                    "id": r[0],
                    "ts": r[1],
                    "prompt": _dec(user_id, r[2])[:200],
                    "response": _dec(user_id, r[3])[:300],
                }
                for r in rows
            ]
        finally:
            conn.close()

    def get_verification_stats(self) -> dict:
        """Statistikk over verifikasjonshistorikk."""
        conn = _get_conn()
        try:
            total = conn.execute("SELECT COUNT(*) FROM verifications").fetchone()[0]
            by_verdict = dict(conn.execute(
                "SELECT verdict, COUNT(*) FROM verifications GROUP BY verdict"
            ).fetchall())
            avg_confidence = conn.execute(
                "SELECT AVG(confidence) FROM interactions WHERE confidence != 0.5"
            ).fetchone()[0]
            return {
                "total_verifications": total,
                "by_verdict": by_verdict,
                "avg_confidence_verified": round(avg_confidence, 3) if avg_confidence else None,
            }
        finally:
            conn.close()



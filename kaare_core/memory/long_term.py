    def get_stats(self) -> dict:
        """Enkel statistikk — nyttig for debugging."""
        conn = _get_conn()
        try:
            total = conn.execute("SELECT COUNT(*) FROM interactions").fetchone()[0]
            by_outcome = conn.execute(
                "SELECT outcome, COUNT(*) FROM interactions GROUP BY outcome"
            ).fetchall()
            by_feedback = conn.execute(
                "SELECT feedback, COUNT(*) FROM interactions GROUP BY feedback"
            ).fetchall()
            return {
                "total": total,
                "by_outcome": dict(by_outcome),
                "by_feedback": dict(by_feedback),
            }
        finally:
            conn.close()


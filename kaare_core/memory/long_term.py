# Nøkkelord som tyder på at brukeren er misfornøyd
_NEGATIVE_PHRASES = [
    "nei", "feil", "ikke det", "galt", "feil enhet", "stopp",
    "ikke riktig", "det var ikke", "mener ikke", "mente ikke",
]

# Nøkkelord som tyder på at brukeren er fornøyd
_POSITIVE_PHRASES = [
    "takk", "perfekt", "bra", "supert", "riktig", "akkurat", "ja takk",
    "stemmer", "korrekt", "flott",
]


def _utc_ts() -> str:
    return datetime.now(timezone.utc).isoformat(timespec="seconds")


def _get_conn() -> sqlite3.Connection:
    """Åpner tilkobling med WAL-modus (trygg for concurrent access)."""
    DB_PATH.parent.mkdir(parents=True, exist_ok=True)
    conn = sqlite3.connect(str(DB_PATH), check_same_thread=False)
    conn.execute("PRAGMA journal_mode=WAL")
    conn.execute("PRAGMA synchronous=NORMAL")
    return conn


def _migrate(conn: sqlite3.Connection) -> None:
    """Legg til nye kolonner og fiks constraints i eksisterende database uten å miste data."""
    def cols(table: str) -> set:
        return {row[1] for row in conn.execute(f"PRAGMA table_info({table})").fetchall()}

    def table_exists(name: str) -> bool:
        return bool(conn.execute(
            "SELECT name FROM sqlite_master WHERE type='table' AND name=?", (name,)
        ).fetchone())

    changed = False
    existing_int = cols("interactions")
    if existing_int and "user_id" not in existing_int:
        conn.execute("ALTER TABLE interactions ADD COLUMN user_id TEXT NOT NULL DEFAULT 'global'")
        changed = True

    existing_ver = cols("verifications")
    if existing_ver and "user_id" not in existing_ver:
        conn.execute("ALTER TABLE verifications ADD COLUMN user_id TEXT NOT NULL DEFAULT 'global'")
        changed = True

    existing_ep = cols("episodes")
    if existing_ep and "user_id" not in existing_ep:
        conn.execute("ALTER TABLE episodes ADD COLUMN user_id TEXT NOT NULL DEFAULT 'global'")
        changed = True

    # Migrate stm_daily_summary: UNIQUE(date) → UNIQUE(date, user_id)
    if table_exists("stm_daily_summary"):
        indexes = {row[1] for row in conn.execute("PRAGMA index_list(stm_daily_summary)").fetchall()}
        if "idx_stm_daily_summary_user_date" not in indexes:
            conn.executescript("""
                CREATE TABLE IF NOT EXISTS stm_daily_summary_new (
                    id                INTEGER PRIMARY KEY AUTOINCREMENT,
                    date              TEXT    NOT NULL,
                    user_id           TEXT    NOT NULL DEFAULT 'global',
                    summary           TEXT    NOT NULL,
                    interaction_count INTEGER DEFAULT 0,
                    ts_created        TEXT    NOT NULL,
                    UNIQUE(date, user_id)
                );
                INSERT INTO stm_daily_summary_new (date, user_id, summary, interaction_count, ts_created)
                    SELECT date, user_id, summary, interaction_count, ts_created
                    FROM stm_daily_summary;
                DROP TABLE stm_daily_summary;
                ALTER TABLE stm_daily_summary_new RENAME TO stm_daily_summary;
                CREATE INDEX IF NOT EXISTS idx_stm_daily_summary_date ON stm_daily_summary(date);
                CREATE INDEX IF NOT EXISTS idx_stm_daily_summary_user_date ON stm_daily_summary(date, user_id);
            """)
            changed = True

    if changed:
        conn.commit()


def _init_db() -> None:
    """Oppretter tabeller hvis de ikke finnes, og kjører migration."""
    conn = _get_conn()
    try:
        _migrate(conn)          # legg til manglende kolonner FØR schema-script
        conn.executescript(_SCHEMA)
        conn.commit()
    finally:
        conn.close()


class LongTermMemory:
    """
    Asynkron langtidsminne-logger.

    Bruk:
        ltm = LongTermMemory()
        interaction_id = await ltm.log_interaction(prompt=..., source=..., ...)
        await ltm.update_response(interaction_id, response=..., outcome=...)
        await ltm.update_feedback(interaction_id, feedback="positive")

    Alt skrives i bakgrunnen – blokkerer aldri svar-stien.
    """

    def __init__(self) -> None:
        _init_db()
        self._last_id: dict[str, Optional[int]] = {}   # per user_id
        self._last_prompt: dict[str, str] = {}          # per user_id

    # -----------------------------------------------------------------------
    # Skriving (asynkron, fire-and-forget)
    # -----------------------------------------------------------------------

    async def log_interaction(
        self,
        *,
        prompt: str,
        user_id: str = USER_GLOBAL,
        source: str = "",
        intent: str = "",
        entity_id: str = "",
        action: str = "",
        model_used: str = "",
    ) -> Optional[int]:
        """
        Logger en ny interaksjon. Returnerer interaction_id.
        Skrives asynkront – kast ikke bort tid på å vente.
        """
        try:
            interaction_id = await asyncio.get_event_loop().run_in_executor(
                None,
                self._write_interaction,
                prompt, user_id, source, intent, entity_id, action, model_used,
            )
            self._last_id[user_id] = interaction_id
            self._last_prompt[user_id] = prompt
            return interaction_id
        except Exception as e:
            logger.warning("LTM: log_interaction feilet: %s", e)
            return None

    def _write_interaction(
        self,
        prompt: str,
        user_id: str,
        source: str,
        intent: str,
        entity_id: str,
        action: str,
        model_used: str,
    ) -> int:
        uid = user_id or USER_GLOBAL
        conn = _get_conn()
        try:
            cur = conn.execute(
                """INSERT INTO interactions
                   (ts, user_id, prompt, source, intent, entity_id, action, model_used, outcome, feedback)
                   VALUES (?, ?, ?, ?, ?, ?, ?, ?, 'pending', 'unknown')""",
                (_utc_ts(), uid, _enc(uid, prompt or ""), source or "",
                 intent or "", entity_id or "", action or "", model_used or ""),
            )
            conn.commit()
            return cur.lastrowid
        finally:
            conn.close()

    def mark_interactions(
        self, ids: list[int], verdict: str, user_id: str = USER_GLOBAL
    ) -> int:
        """
        Sets feedback and confidence on specific interactions.
        verdict: 'verified' | 'denied' | 'test'
        Returns number of rows updated.
        """
        if not ids:
            return 0
        confidence_map = {"verified": 0.9, "denied": 0.1, "test": 0.0}
        feedback_map = {"verified": "verified", "denied": "denied_general", "test": "test"}
        new_confidence = confidence_map.get(verdict, 0.5)
        new_feedback = feedback_map.get(verdict, verdict)
        conn = _get_conn()
        try:
            placeholders = ",".join("?" * len(ids))
            cur = conn.execute(
                f"UPDATE interactions SET feedback=?, confidence=?"
                f" WHERE id IN ({placeholders}) AND user_id=?",
                [new_feedback, new_confidence, *ids, user_id],
            )
            conn.commit()
            return cur.rowcount
        finally:
            conn.close()

    async def update_response(
        self,
        interaction_id: Optional[int],
        *,
        response: str,
        outcome: str,          # 'success' | 'error' | 'clarification' | 'llm_fallback' | 'vlm' | 'test'
        intent: str = "",
        entity_id: str = "",
        action: str = "",
        repaired: bool = False,
        model_used: str = "",
    ) -> None:
        """Oppdaterer rad med svar og utfall. Kalles etter at svar er klart."""
        if interaction_id is None:
            return
        try:
            await asyncio.get_event_loop().run_in_executor(
                None,
                self._write_response,
                interaction_id, response, outcome, intent, entity_id, action, repaired, model_used,
            )
        except Exception as e:
            logger.warning("LTM: update_response feilet: %s", e)

    def _write_response(
        self,
        interaction_id: int,
        response: str,
        outcome: str,
        intent: str,
        entity_id: str,
        action: str,
        repaired: bool,
        model_used: str,
    ) -> None:
        conn = _get_conn()
        try:
            row = conn.execute("SELECT user_id FROM interactions WHERE id=?", (interaction_id,)).fetchone()
            uid = row[0] if row else USER_GLOBAL
            conn.execute(
                """UPDATE interactions SET
                   response=?, outcome=?, intent=COALESCE(NULLIF(?, ''), intent),
                   entity_id=COALESCE(NULLIF(?, ''), entity_id),
                   action=COALESCE(NULLIF(?, ''), action),
                   repaired=?, model_used=COALESCE(NULLIF(?, ''), model_used)
                   WHERE id=?""",
                (_enc(uid, response or ""), outcome or "unknown", intent or "", entity_id or "", action or "",
                 1 if repaired else 0, model_used or "", interaction_id),
            )
            conn.commit()
        finally:
            conn.close()

    async def log_fallback_session(
        self,
        *,
        ts_start: str,
        ts_end: str,
        turn_count: int,
    ) -> None:
        """Log a completed 9B fallback session to LTM (fire-and-forget)."""
        try:
            await asyncio.get_event_loop().run_in_executor(
                None, self._write_fallback_session, ts_start, ts_end, turn_count,
            )
        except Exception as e:
            logger.warning("LTM: log_fallback_session feilet: %s", e)

    def _write_fallback_session(self, ts_start: str, ts_end: str, turn_count: int) -> None:
        conn = _get_conn()
        try:
            conn.execute(
                "INSERT INTO fallback_sessions (ts_start, ts_end, turn_count) VALUES (?, ?, ?)",
                (ts_start, ts_end, turn_count),
            )
            conn.commit()
        finally:
            conn.close()

    async def update_feedback(
        self,
        interaction_id: Optional[int],
        *,
        feedback: str,   # 'positive' | 'negative' | 'neutral'
    ) -> None:
        """Oppdaterer feedback-felt på en eksisterende interaksjon."""
        if interaction_id is None:
            return
        try:
            await asyncio.get_event_loop().run_in_executor(
                None, self._write_feedback, interaction_id, feedback,
            )
        except Exception as e:
            logger.warning("LTM: update_feedback feilet: %s", e)

    def _write_feedback(self, interaction_id: int, feedback: str) -> None:
        conn = _get_conn()
        try:
            conn.execute(
                "UPDATE interactions SET feedback=? WHERE id=?",
                (feedback, interaction_id),
            )
            conn.commit()
        finally:
            conn.close()

    # -----------------------------------------------------------------------
    # Feedback-deteksjon
    # -----------------------------------------------------------------------

    def detect_verification_response(self, prompt: str) -> Optional[str]:
        """
        Sjekker om prompten er et svar på en verifikasjonsforespørsel.
        Bruker konkrete fraser som ikke kan misforstås som action-feedback.

        Returnerer 'confirmed', 'denied', eller None.
        """
        if not prompt:
            return None
        p = prompt.lower().strip()

        _confirmed = ["det virker", "det er riktig", "det er sant", "stemmer bra", "fungerer bra", "ja det stemmer"]
        _denied    = ["ikke helt", "nei faktisk", "stemmer ikke", "det stemmer ikke", "fungerer ikke bra"]

        if any(phrase in p for phrase in _denied):
            return "denied"
        if any(phrase in p for phrase in _confirmed):
            return "confirmed"
        return None

    def detect_feedback_signal(self, prompt: str, user_id: str = USER_GLOBAL) -> Optional[str]:
        """
        Sjekker om en ny prompt inneholder signal om forrige svar.
        Returnerer 'positive', 'negative', eller None.

        Kalles FØR ny interaksjon logges, mens _last_id peker på forrige for denne brukeren.
        """
        if not prompt or not self._last_id.get(user_id):
            return None

        p = prompt.lower().strip()

        # Negativ: bruker korrigerer eller klager
        if any(phrase in p for phrase in _NEGATIVE_PHRASES):
            return "negative"

        # Gjentakelse: veldig lik forrige prompt (bruker prøver igjen)
        last_prompt = self._last_prompt.get(user_id, "")
        if last_prompt:
            prev = last_prompt.lower().strip()
            # Enkel likhet: mer enn 60% av ordene er felles
            prev_words = set(prev.split())
            curr_words = set(p.split())
            if prev_words and curr_words:
                overlap = len(prev_words & curr_words) / max(len(prev_words), len(curr_words))
                if overlap > 0.6 and p != prev:
                    return "negative"

        # Positiv: bruker bekrefter
        if any(phrase in p for phrase in _POSITIVE_PHRASES):
            return "positive"

        return None

    # -----------------------------------------------------------------------
    # Søk og lesing
    # -----------------------------------------------------------------------

    def search_interactions(
        self, query: str, limit: int = 8, user_id: Optional[str] = None
    ) -> list[dict]:
        """
        Søker i interaksjonshistorikk etter relevante episoder.
        Matcher på tvers av prompt, response, entity_id og action.
        Returnerer de N mest relevante, nyeste først.
        Enkel nøkkelord-matching — Qdrant tar over i semantisk lag.
        user_id=None søker på tvers av alle brukere.
        """
        if not query or not query.strip():
            return []

        keywords = [w.strip().lower() for w in query.split() if len(w.strip()) > 2]
        if not keywords:
            return []

        conn = _get_conn()
        try:
            conditions = []
            params: list = []

            if user_id is not None:
                conditions.append("user_id = ?")
                params.append(user_id)

            for kw in keywords:
                pattern = f"%{kw}%"
                conditions.append(
                    "(LOWER(prompt) LIKE ? OR LOWER(response) LIKE ? "
                    "OR LOWER(entity_id) LIKE ? OR LOWER(action) LIKE ?)"
                )
                params.extend([pattern, pattern, pattern, pattern])

            where = " AND ".join(conditions) if conditions else "1=1"
            params.append(limit)

            cur = conn.execute(
                f"""SELECT id, ts, user_id, prompt, intent, entity_id, action,
                           response, outcome, feedback, confidence
                    FROM interactions
                    WHERE {where}
                    ORDER BY id DESC
                    LIMIT ?""",
                params,
            )
            cols = [c[0] for c in cur.description]
            rows = [dict(zip(cols, row)) for row in cur.fetchall()]
            return [_dec_row(r, ["prompt", "response"]) for r in rows]
        finally:
            conn.close()

    def get_recent(self, limit: int = 20, user_id: Optional[str] = None) -> list[dict]:
        """
        Henter siste N interaksjoner.
        user_id=None returnerer alle brukere.
        """
        conn = _get_conn()
        try:
            if user_id is not None:
                cur = conn.execute(
                    """SELECT id, ts, user_id, prompt, intent, entity_id, action, response, outcome, feedback
                       FROM interactions WHERE user_id = ? ORDER BY id DESC LIMIT ?""",
                    (user_id, limit),
                )
            else:
                cur = conn.execute(
                    """SELECT id, ts, user_id, prompt, intent, entity_id, action, response, outcome, feedback
                       FROM interactions ORDER BY id DESC LIMIT ?""",
                    (limit,),
                )
            cols = [c[0] for c in cur.description]
            rows = [dict(zip(cols, row)) for row in cur.fetchall()]
            return [_dec_row(r, ["prompt", "response"]) for r in rows]
        finally:
            conn.close()

